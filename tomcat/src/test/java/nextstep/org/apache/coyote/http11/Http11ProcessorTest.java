    @Test
    void login_cookie() {
        // given
        final String httpRequest = String.join("\r\n",
                "POST /login HTTP/1.1 ",
                "Host: localhost:8080 ",
                "Connection: keep-alive ",
                "Content-Length: 30",
                "",
                "account=gugu&password=password",
                "");

        final var socket = new StubSocket(httpRequest);
        final Http11Processor processor = new Http11Processor(socket);

        // when
        processor.process(socket);

        // then
        assertAll(
                () -> assertThat(socket.output()).contains("HTTP/1.1 302 FOUND"),
                () -> assertThat(socket.output()).contains("Set-Cookie")
        );
    }

    @Test
    void login_redirectToIndex_withCookie() throws IOException {
        // given
        final SessionManager sessionManager = new SessionManager();
        final Session session = new Session("1234");
        final User user = new User("brorae", "password", "brorae@naver.com");
        session.setAttribute("user", user);
        sessionManager.add(session);

        final String httpRequest = String.join("\r\n",
                "GET /login HTTP/1.1 ",
                "Host: localhost:8080 ",
                "Connection: keep-alive ",
                "Cookie: JSESSIONID=1234",
                "",
                "");

        final var socket = new StubSocket(httpRequest);
        final Http11Processor processor = new Http11Processor(socket);

        // when
        processor.process(socket);

        // then
        final URL resource = getClass().getClassLoader().getResource("static/index.html");
        var expected = "HTTP/1.1 302 FOUND \r\n" +
                "Content-Type: text/html \r\n" +
                "Content-Length: 5564 \r\n" +
                "\r\n" +
                new String(Files.readAllBytes(new File(resource.getFile()).toPath()));

        assertThat(socket.output()).isEqualTo(expected);
    }

